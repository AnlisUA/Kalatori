use thiserror::Error;
use uuid::Uuid;

use crate::types::WebhookEvent;

use super::DaoExecutor;

#[derive(Debug, Error)]
pub enum DaoWebhookEventError {
    #[error("Database error during webhook event operation")]
    DatabaseError,
}

pub trait DaoWebhookEventMethods: DaoExecutor + 'static {
    async fn create_webhook_event(
        &self,
        event: impl Into<WebhookEvent>,
    ) -> Result<WebhookEvent, DaoWebhookEventError> {
        let event = event.into();

        let query = sqlx::query_as::<_, WebhookEvent>(
            "INSERT INTO webhook_events (id, entity_id, payload, sent, created_at, updated_at)
             VALUES (?, ?, ?, ?, ?, ?)
             RETURNING *",
        )
        .bind(event.id)
        .bind(event.entity_id)
        .bind(event.payload)
        .bind(event.sent)
        .bind(event.created_at)
        .bind(event.updated_at);

        self.fetch_one(query)
            .await
            .map_err(|e| {
                tracing::debug!(
                    error.category = "dao.webhook_event",
                    error.operation = "create_webhook_event",
                    error.source = ?e,
                    event.id = %event.id,
                    event.entity_id = %event.entity_id,
                    "Failed to create webhook event"
                );

                DaoWebhookEventError::DatabaseError
            })
    }

    async fn get_webhook_events_to_send(
        &self,
        limit: u32,
    ) -> Result<Vec<WebhookEvent>, DaoWebhookEventError> {
        if limit == 0 {
            return Ok(Vec::new());
        }

        let query = sqlx::query_as::<_, WebhookEvent>(
            "WITH ranked_events AS (
              SELECT
                id,
                entity_id,
                payload,
                sent,
                created_at,
                updated_at,
                ROW_NUMBER() OVER (
                  PARTITION BY entity_id
                  ORDER BY created_at ASC, id ASC
                ) as rn
              FROM webhook_events
              WHERE sent = 0
            )
            SELECT id, entity_id, payload, sent, created_at, updated_at
            FROM ranked_events
            WHERE rn = 1
            ORDER BY created_at ASC, id ASC
            LIMIT ?;",
        )
        .bind(limit);

        self.fetch_all(query)
            .await
            .map_err(|_| DaoWebhookEventError::DatabaseError)
    }

    async fn mark_webhook_event_as_sent(
        &self,
        event_id: Uuid,
    ) -> Result<WebhookEvent, DaoWebhookEventError> {
        let query = sqlx::query_as::<_, WebhookEvent>(
            "UPDATE webhook_events
             SET sent = 1, updated_at = ?
             WHERE id = ?
             RETURNING *",
        )
        .bind(chrono::Utc::now().naive_utc())
        .bind(event_id);

        self.fetch_one(query)
            .await
            .map_err(|_| DaoWebhookEventError::DatabaseError)
    }
}

impl<T: DaoExecutor + 'static> DaoWebhookEventMethods for T {}

