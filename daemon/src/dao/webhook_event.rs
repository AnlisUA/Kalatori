    async fn test_create_webhook_event() {
        let dao = create_test_dao().await;

        let invoice_id = Uuid::new_v4();

        // Create a webhook event for this invoice
        let event = default_webhook_event(invoice_id);
        let event_id = event.id;
        let event_timestamp = event.timestamp;

        let webhook_event = dao
            .create_webhook_event(event)
            .await
            .unwrap();

        // Verify the webhook event was created correctly
        assert_eq!(webhook_event.id, event_id);
        assert!(!webhook_event.sent);
        assert_eq!(
            webhook_event.created_at,
            event_timestamp
        );
        assert_eq!(
            webhook_event.updated_at,
            event_timestamp
        );

        // Verify the payload can be deserialized
        let deserialized: crate::types::GenericEvent<PublicInvoice> =
            serde_json::from_value(webhook_event.payload).unwrap();
        assert_eq!(deserialized.id, event_id);
        assert_eq!(deserialized.payload.id, invoice_id);
    }

