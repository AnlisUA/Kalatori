    #[tokio::test]
    async fn test_get_webhook_events_to_send_deterministic_ordering() {
        let dao = create_test_dao().await;

        // Create multiple events with the exact same timestamp
        let timestamp = Utc::now();
        let mut event_ids = Vec::new();
        let invoice_id = Uuid::new_v4();

        for _ in 0..3 {
            let mut event = default_webhook_event(invoice_id);
            event.timestamp = timestamp; // Force same timestamp
            event_ids.push(event.id);
            dao.create_webhook_event(event)
                .await
                .unwrap();
        }

        // Get events to send multiple times - should always return the same event
        // (lowest id)
        let events1 = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        let events2 = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        let events3 = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();

        assert_eq!(events1.len(), 1);
        assert_eq!(events2.len(), 1);
        assert_eq!(events3.len(), 1);

        // All should return the same event
        assert_eq!(events1[0].id, events2[0].id);
        assert_eq!(events2[0].id, events3[0].id);

        // Should be the one with the smallest UUID (due to ORDER BY id ASC
        // tie-breaking)
        let mut sorted_ids = event_ids.clone();
        sorted_ids.sort();
        assert_eq!(events1[0].id, sorted_ids[0]);
    }

    #[tokio::test]
    async fn test_mark_webhook_event_as_sent() {
        let dao = create_test_dao().await;

        let event = default_webhook_event(Uuid::new_v4());
        let event_id = event.id;
        let original_timestamp = event.timestamp;

        let created = dao
            .create_webhook_event(event)
            .await
            .unwrap();
        assert!(!created.sent);
        assert_eq!(created.created_at, original_timestamp);
        assert_eq!(created.updated_at, original_timestamp);

        // Small delay to ensure updated_at will be different
        tokio::time::sleep(tokio::time::Duration::from_millis(10)).await;

        // Mark as sent
        let updated = dao
            .mark_webhook_event_as_sent(event_id)
            .await
            .unwrap();

        // Verify it's marked as sent
        assert!(updated.sent);
        assert_eq!(updated.id, event_id);

        // Verify timestamps
        assert_eq!(updated.created_at, original_timestamp); // created_at unchanged
        assert!(updated.updated_at > original_timestamp); // updated_at changed

        // Verify it no longer appears in get_webhook_events_to_send
        let events = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        assert_eq!(events.len(), 0);
    }

    #[tokio::test]
    async fn test_mark_webhook_event_as_sent_after_marking_next_event_appears() {
        let dao = create_test_dao().await;

        let invoice_id = Uuid::new_v4();

        // Create 3 events for the same invoice
        let event1 = default_webhook_event(invoice_id);
        let event1_id = event1.id;
        dao.create_webhook_event(event1)
            .await
            .unwrap();

        tokio::time::sleep(tokio::time::Duration::from_millis(10)).await;

        let event2 = default_webhook_event(invoice_id);
        let event2_id = event2.id;
        dao.create_webhook_event(event2)
            .await
            .unwrap();

        tokio::time::sleep(tokio::time::Duration::from_millis(10)).await;

        let event3 = default_webhook_event(invoice_id);
        let event3_id = event3.id;
        dao.create_webhook_event(event3)
            .await
            .unwrap();

        // Get first event to send
        let events = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        assert_eq!(events.len(), 1);
        assert_eq!(events[0].id, event1_id);

        // Mark first event as sent
        dao.mark_webhook_event_as_sent(event1_id)
            .await
            .unwrap();

        // Get next event - should now return event2
        let events = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        assert_eq!(events.len(), 1);
        assert_eq!(events[0].id, event2_id);

        // Mark second event as sent
        dao.mark_webhook_event_as_sent(event2_id)
            .await
            .unwrap();

        // Get next event - should now return event3
        let events = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        assert_eq!(events.len(), 1);
        assert_eq!(events[0].id, event3_id);

        // Mark third event as sent
        dao.mark_webhook_event_as_sent(event3_id)
            .await
            .unwrap();

        // No more events to send
        let events = dao
            .get_webhook_events_to_send(10)
            .await
            .unwrap();
        assert_eq!(events.len(), 0);
    }
